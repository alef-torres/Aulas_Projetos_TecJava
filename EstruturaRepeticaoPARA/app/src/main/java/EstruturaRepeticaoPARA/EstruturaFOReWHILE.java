package EstruturaRepeticaoPARA;

public class EstruturaFOReWHILE {
  
    public static void main(String[] args) {
      
        int j=0;
        while(j <10){
        
        for(int i=10;i>=1;i--){
            
            System.out.println(i);
        
        }
        
        for(int i=2;i<=9;i++){
            
            System.out.println(i);
        
        }
        
        j++;
    }
        
        /*
            estrutura de repeticao FOR mais WHILE para fazer loops dentro do 
            codigo, sao muito uteis para fazer coisas longas.
        
        
        **/
        
        
        
    }
}
